package edu.cornell.slicktest;

public class BattleAnimationResult {
	public boolean needFurthurAnimation;
	public int screenOffsetX;
	public int screenOffsetY;
	public BattleAnimationResult(boolean needFurthurAnimation,
			int screenOffsetX, int screenOffsetY) {
		this.needFurthurAnimation = needFurthurAnimation;
		this.screenOffsetX = screenOffsetX;
		this.screenOffsetY = screenOffsetY;
	}
	
	
}
